/**
 * Article model.
 * */
export class Article {
  /**
   * ID.
   */
  public id: number;

  /**
   * Article title.
   */
  public title: string;

  /**
   * Author of the article.
   */
  public author: string;

  /**
   * Date of the article.
   */
  public date: number;

  /**
   * Article category.
   */
  public category: string;

  /**
   * Article image's url.
   */
  public image: string;

  /**
   * Text of the article.
   */
  public text: string;
}
